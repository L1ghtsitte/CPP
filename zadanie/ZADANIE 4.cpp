#include <iostream>

int main()
{
	int num1;
	int num2;

	std::cout << "Enter the first number: ";
	std::cin >> num1;
	std::cout << "Enter the second number: ";
	std::cin >> num2;

	std::cout << "Difference: " << num1 - num2 << "\n";
	std::cout << "Sum: " << num1 + num2 << "\n";
	std::cout << "Product: " << num1 * num2 << "\n";
}
