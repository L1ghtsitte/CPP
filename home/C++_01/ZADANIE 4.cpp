#include <iostream>

int main()
{
	int a;
	int b;

	std::cout << "Enter first number: ";
	std::cin >> a;
	std::cout << "Enter second number: ";
	std::cin >> b;
	std::cout << "difference: " << a - b << "\n";
	std::cout << "sum: " << a + b << "\n";
	std::cout << "product of numbers : " << a * b << "\n";
}
